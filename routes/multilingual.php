<?php

Route::group(['prefix' => '{locale}'], function() {

    /*
    |--------------------------------------------------------------------------
    | Category routes
    |--------------------------------------------------------------------------
    |
    */

    Route::get('/shop/{category}', 'Shop\CategoryController@show');

    /*
    |--------------------------------------------------------------------------
    | Product routes
    |--------------------------------------------------------------------------
    |
    */

    Route::get('/shop/{category}/{product}', 'Shop\ProductController@show');

    /*
    |--------------------------------------------------------------------------
    | Cart routes
    |--------------------------------------------------------------------------
    |
    */

    Route::get('/cart', 'Shop\CartController@index');
    
    /*
    |--------------------------------------------------------------------------
    | Checkout routes
    |--------------------------------------------------------------------------
    |
    */
    
    Route::get('/checkout/{step?}', 'Shop\CheckoutController@index')->name('checkout');

    /*
    |--------------------------------------------------------------------------
    | Page routes
    |--------------------------------------------------------------------------
    |
    */

    Route::get('{segments?}', 'Front\PageController@getPage')->where('segments', '.*');

});
